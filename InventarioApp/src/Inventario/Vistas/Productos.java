 public void cargarTablaProductos() {
    DefaultTableModel modelo = (DefaultTableModel) TablaProductos.getModel();
    modelo.setRowCount(0); 

    for (Producto p : productoDAO.obtenerTodosLosProductos()) {
        modelo.addRow(new Object[]{
            p.getId(),
            p.getNombre(),
            p.getDescripcion(),
            p.getPrecioVenta(),
            p.getStock()
        });
    }
}

